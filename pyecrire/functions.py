#
# Strips string of all non-English characters
#

def simplifyString(inStr):

    tmpStr = inStr.lower()
    outStr = tmpStr

    return outStr


#
# Creates a file handle from a string
#

def makeHandle(inStr):

    tmpStr = simplifyString(inStr)
    outStr = tmpStr

    return outStr
